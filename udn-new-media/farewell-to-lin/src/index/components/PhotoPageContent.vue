      <p><br></p>
      <p>那段時間，林懷民感受到台灣社會的快速變化，創作出1984年的《春之祭禮・台北一九八四》，背幕上投影著代表工業化的怪手、推土機、鏟平的老厝和新建的高樓，舞者在高樓前舞著，身影顯得渺小，擠壓、交疊的肢體，彷彿被後方的怪手吞噬，是林懷民對現代化最深的反思。</p>
      <p><br></p>
      <p>雲門舞集當時也跟著社會的步調一起高速發展，隨舞團演出愈來愈多，加上林懷民在學校任教的系務壓力，隨之而來的是忙亂、沒有時間停下腳步思考。</p>
      <p><br></p>
      <p>1988年林懷民毅然決然休團、暫時停下腳步。雲門舞集暫停3年後，他遇到一位計程車司機問他為何停掉雲門，並在他述說種種辛苦後，說「每個行業都很辛苦，但台灣還是需要雲門舞集」。林懷民說，他那時「覺得無比羞愧」，之後重啟舞團。</p>
      <p><br></p>
